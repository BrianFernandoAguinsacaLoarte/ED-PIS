    private void limpiar() {
        cc.setCurso(null);
        cc.setLista(new LinkedList<>());
        cargarTabla();
        cc.setCurso(null);
        cc.setIndex(-1);
        try {
            UtilVista.cargarParalelos(cbxParalelo);
            UtilVista.cargarCiclo(cbxCiclo);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void cargarTabla() {
        mtg.setCursos(cc.getLista());
        jTableCursos.setModel(mtg);
        jTableCursos.updateUI();
    }

    public void guardar() {
        if (validar()) {
            try {
                Curso curso = new Curso();
                // Obtener el Ciclo seleccionado del ComboBox y asignar su clave for√°nea

                Ciclo cicloSeleccionado = (Ciclo) cbxCiclo.getSelectedItem();
                Integer idCicloSeleccionado = cicloSeleccionado.getId();

                Paralelo paraleloSelecionado = (Paralelo) cbxParalelo.getSelectedItem();
                Integer idParaleloSeleccionado = paraleloSelecionado.getId();

                System.out.println("Ciclo: " + idCicloSeleccionado);
                System.out.println("Paralelo: " + idParaleloSeleccionado);

                curso.setId_ciclo(idCicloSeleccionado);
                curso.setId_paralelo(idParaleloSeleccionado);

                cc.setCurso(curso);
                cc.guardar(curso);
                cc.setCurso(null);
                limpiar();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error al guardar el Curso: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "");
        }
    }

    private void modificar() {
        try {
            int filaSeleccionada = jTableCursos.getSelectedRow();

            if (filaSeleccionada == -1) {
                JOptionPane.showMessageDialog(null, "Seleccione un ciclo para modificar");
                return;
            }

            Curso cursoSeleccionado = mtg.getCursos().get(filaSeleccionada);

            Ciclo cicloSeleccionado = (Ciclo) cbxCiclo.getSelectedItem();
            Integer idcicloSeleccionado = cicloSeleccionado.getId();

            Paralelo paraleloSelecionado = (Paralelo) cbxParalelo.getSelectedItem();
            Integer idParaleloSeleccionado = paraleloSelecionado.getId();

            cursoSeleccionado.setId_ciclo(idcicloSeleccionado);
            cursoSeleccionado.setId_paralelo(idParaleloSeleccionado);

            cc.modificar(cursoSeleccionado);

            limpiar();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al modificar el ciclo: " + e.getMessage());
        }
    }

    private void cargarVista() {
        cc.setIndex(jTableCursos.getSelectedRow());
        if (cc.getIndex().intValue() < 0) {
            JOptionPane.showMessageDialog(null, "Selecciona una fila",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                cc.setCurso(mtg.getCursos().get(cc.getIndex()));
                UtilVista.setComboCiclo(cbxCiclo, cc.getCurso().getId_ciclo());
                UtilVista.setComboParalelo(cbxParalelo, cc.getCurso().getId_paralelo());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

